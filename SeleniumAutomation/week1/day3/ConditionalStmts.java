package week1.day3;

public class ConditionalStmts {

	public static void main(String[] args) {
		
		
		  int n1=25;  
		  int n2=39;  
		  if(n1!=n2){  
			  System.out.println("25!=39");
			  if(n1<n2){  
				  System.out.println("25<39");  
				  if(n1<=n2) { 
					  System.out.println("25<=39");
					  } 
				  }  
			  }  
		  else { 
			  System.out.println("Does not fall under any condition"); 
			  }
		 	 

		
		
	}

}
